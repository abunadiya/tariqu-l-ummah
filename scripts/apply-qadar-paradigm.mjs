import fs from 'fs';

function patch(file, patches) {
  let s = fs.readFileSync(file, 'utf8');
  let n = 0;
  for (const [from, to] of patches) {
    if (!s.includes(from)) {
      console.warn(`MISS in ${file}:`, from.slice(0, 80).replace(/\n/g, ' '));
      continue;
    }
    s = s.replace(from, to);
    n++;
  }
  fs.writeFileSync(file, s);
  console.log(`${file}: ${n}/${patches.length}`);
}

