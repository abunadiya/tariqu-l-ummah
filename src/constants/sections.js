const SECTION_BY_PATH = Object.fromEntries(
  ENCYCLOPEDIA_SECTIONS.map((section) => [section.to, section]),
);

export const getSectionByPath = (path) => SECTION_BY_PATH[path] ?? null;

export const getRelatedSections = (path) => {
  const section = getSectionByPath(path);

  if (!section) {
    return [];
  }

  return section.related
    .map(({ to, hint }) => {
      const relatedSection = getSectionByPath(to);

      if (!relatedSection) {
        return null;
      }

      return {
        id: relatedSection.id,
        to: relatedSection.to,
        title: relatedSection.title,
        period: relatedSection.period,
        icon: relatedSection.icon,
        hint,
      };
    })
    .filter(Boolean);
};

export default ENCYCLOPEDIA_SECTIONS;
